import { CHECKLIST } from "./checklist";

export function buildPrompt(mediaType) {
  const isPhoto = !mediaType.includes("pdf");
  return `Ты — эксперт-аудитор. Проанализируй документ и верни ТОЛЬКО JSON (без markdown, без backticks).

${isPhoto ? `КРИТИЧЕСКИ ВАЖНО — ЭТО ФОТОГРАФИЯ/СКАН:
- Сначала ВНИМАТЕЛЬНО прочитай ВСЁ содержимое, определи все числа и текст
- Разделители тысяч (пробелы): "7 791 809" = 7791809
- Запятая = десятичный разделитель: "3 407,00" = 3407.00
- В таблицах: читай КАЖДЫЙ столбец отдельно
- Сумма прописью — контрольная проверка
` : ''}

МАТЕМАТИКА: Проверки 1.1–1.6 выполняются ПРОГРАММОЙ автоматически. 
Твоя задача — ТОЧНО извлечь числа в поля items, total_sum, nds_sum, total_words.
Для items: qty, price, sum — числа БЕЗ пробелов, с точкой.
Для критериев 1.1–1.6 поставь status: "skip".

ПРАВИЛА КРИТЕРИЕВ:
1.1–1.6: Поставь "skip" — математика проверяется программой.
2.1: ИНН=10 или 12 цифр, КПП=9 цифр. Контрольная сумма ИНН (10 цифр): коэфф [2,4,10,3,5,9,4,6,8]. Неверно → "fail".
2.2: QR-код: если есть, проверь соответствие суммы. Нет QR → "skip".
2.3: QR не считывается? Нет QR → "skip".
3.1: Сроки реалистичны? Не указаны → "skip".
3.3: Скидка >40%? Нет → "skip".
3.5: Тех.хар-ки верны? Да → "ok".
4.1: Грамматика? Укажи КОНКРЕТНЫЕ ошибки. Нет → "ok".
4.4: Следы редактирования? Нет → "ok".
4.6: Разные версии? Один документ → "skip".
5.1: Оцени по реквизитам (ИНН, адрес, банк). Всё есть → "ok".
5.2: Признаки ликвидации? Нет данных → "skip".

JSON:
{
  "extracted": {
    "doc_type":"","doc_number":"","doc_date":"",
    "supplier_name":"","supplier_inn":"","supplier_kpp":"",
    "supplier_address":"","supplier_bank":"",
    "buyer_name":"","buyer_inn":"",
    "items":[{"name":"","qty":"число","unit":"","price":"число","sum":"число"}],
    "total_sum":"число","total_words":"прописью","nds_sum":"число или null","nds_rate":"",
    "has_signature":false,"has_stamp":false,"has_logo":false,"has_qr":false,
    "validity_period":"","delivery_terms":"","contact_person":""
  },
  "checks":[
${CHECKLIST.map(c => `    {"id":"${c.id}","status":"ok/warn/fail/skip","comment":"пояснение"}`).join(",\n")}
  ]
}`;
}
